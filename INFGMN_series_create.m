
function dumpname = INFGMN_series_create(DS, ...
        normalize_list, delta_list, tau_list, tmax_list, maxStableNC_list)
    %% constants
    combinations = length(normalize_list) ...
        * length(delta_list) * length(tau_list) ...
        * length(tmax_list) * length(maxStableNC_list);
    %% alocate dumps struct
    dumps = struct( ...
            'normalize', NaN, ...
            'delta', NaN, ...
            'tau', NaN, ...
            'tmax', NaN, ...
            'spmin', NaN, ...
            'maxStableNC', NaN, ...
            ...
            'cputime', 0, ...
            'progress', 1, ...
            'stats', cell(1, combinations), ...
            'mean_NC', NaN, ...
            'mean_NC_1', NaN, ...
            'mean_NC_2', NaN, ...
            'mean_NC_3', NaN, ...
            'mamdani_RMSE', Inf, ...
            'sugeno_RMSE', Inf );
    %% begin test
    ii = 0;
    for normalize = normalize_list
    for delta = delta_list
    for tau = tau_list
    for tmax = tmax_list
    for maxStableNC = maxStableNC_list
        ii = ii + 1;
        dumps(ii).normalize = normalize;
        dumps(ii).delta = delta;
        dumps(ii).tau = tau;
        dumps(ii).tmax = tmax;
        dumps(ii).spmin = tmax / maxStableNC;
        dumps(ii).maxStableNC = maxStableNC;
    end
    end
    end
    end
    end
    dumpname = ['dumps/' 'DynNLSys' ...
        '_1x' num2str(combinations) ...
        '_' datestr(now) '.mat'];
    save(dumpname, 'DS', 'dumps');
end
